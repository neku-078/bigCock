				<p class="title" id="popularity">༺༒&nbsp;盛&nbsp;&nbsp;行&nbsp;༒༻</p>
				<p class="content"><span style="font-size: 35px;">賓治時代（1600s）</span><br>
									在1600年，英國商人在英女王伊莉莎白一世的授權下創立東印度公司，開始了往亞洲地區拓展勢力的歷史。當時的英國水手們為了解決缺乏啤酒和葡萄酒的問題，開始將檸檬、香料、糖及水混進酒中，這種飲料被印度人以「Panch」（印度語「五」的意思）稱呼，但被英國水手們誤傳為「Punch」。不過無論如何，這段歷史都開啟了雞尾酒的興盛，並創製出賓治系列雞尾酒。<br><br>
									<span style="font-size: 35px;">殖民時代（1700s）</span><br>
									在英國人殖民美國的初期，由於生活環境並未完善發展，不少人都會藉著酒精作為協助消化、暖化身體、甚至緩解病痛的醫療用途，並以「Julep」稱呼這種將藥草及糖漿加進波本威士忌的飲料。<br><br>
									<span style="font-size: 35px;">維多利亞時代（1800 - 1840）</span><br>
									可謂最代雞尾酒最重要的時代，皆因「雞尾酒」一字就是在這時候正式出現，而且亦是眾多經典雞尾酒誕生的日子。不同於之前，雞尾酒從這時開始慢慢走向花俏化，而調酒師亦步向的專業化，甚至開發確立正式的酒譜。<br><br>
									<span style="font-size: 35px;">黃金時代（1870 - 1919）</span><br>
									「Mixoligist」（調酒師）的稱號在這段時間被正式確立，以茲分別普通的酒吧服務生，令調製雞尾酒進一步專業化，而首本調酒師天書「Harry Johnson's Bartenders Manual」就是在這個黃金時代寫下的，裡面詳細寫下了如何開設及營運一間酒吧。此外，經典的雞尾酒Martini及Manhattan也是在這時候誕生的。<br><br>
									<span style="font-size: 35px;">禁酒時代（1920 - 1930）</span><br>
									隨著酗酒問題日益嚴重，美國政府於1920年在「乾派」的推動下通過了禁酒法令，禁止人民製造、銷售、運輸、進口或出口含酒精飲料，不過卻也因此造就了雞尾酒的爆炸性發展。當時不少秘密酒吧因為禁酒令應運而生，為了掩蓋劣質酒的味道，酒吧商人將大量的糖漿、果汁、梳打水等混入酒中，令雞尾酒更加發揚光大。<br><br>
									<span style="font-size: 35px;">後禁酒時代（1930 - 1945）</span><br>
									雖然禁酒令催生了雞尾酒的強盛發展，但另一方面亦令在黃金時代開始走向專業化的調酒師行業慢慢走下坡，那些自命專業的調酒師視雞尾酒為藝術，而不願為了私酒的劣質味道出賣專業，加上第二次世界大戰爆發，調酒師工作開始由女性接手，比起帶苦澀及嗆喉勁道的雞尾酒，她們更喜歡調製一些味道清新偏甜而且講究漂亮色彩的雞尾酒，形成了另一種新的雞尾酒潮流。<br></p>
