				<p class="content">雞尾酒的特點是原料的組合，有無限多種色彩、味道各不相同，常用的酒類有琴酒、威士忌、白蘭地、龍舌蘭酒、蘭姆酒、苦艾酒、伏特加、葡萄酒等；果汁類有柳橙汁、檸檬汁、薑汁等；汽水類有可樂、七喜、氣泡水、通寧水、薑汁汽水等；有時加入櫻桃、楊梅、柳橙片和檸檬片等裝飾。<br><br>
									除配方組合，亦有不同溫度的雞尾酒，溫熱如湯的熱托迪（Hot Toddy），抑或是像冰沙的德比戴克利（Derby Daiquiri）等。<br><br>
									盛裝雞尾酒的杯子有各式各樣如雞尾酒杯（Cocktail Glass）、柯林斯杯（Collins Glass）、高球杯（Highball Glass）、颶風杯（Hurricane Glass）、經典酒杯（Old-fashioned Glass）等。</p>
