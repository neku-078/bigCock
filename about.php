				<p class="content"><span style="font-size: 35px;">Q1. 常聽到調酒6大基酒，是哪6種？</span><br><br>
				「調酒的基底」，簡稱基酒，廣泛指的六大基酒，包含伏特加 Vodka、蘭姆酒 Rum、琴酒 Gin、龍舌蘭 Tequila、威士忌 Whiskey、白蘭地 Brandy。基酒通常是一杯雞尾酒中占比最高的原料，幾乎所有常見的調酒都是使用一種基酒基底（當然，除了使用了四種基酒的失身之酒長島冰茶）。<br><br>
				<span style="font-size: 35px;">Q2. 調酒杯有差嗎？ 長型雞尾酒 vs 短型雞尾酒</span><br><br>
				調酒新手常見的一個問題，調酒杯有高有低，有什麼差異嗎？可以將杯子分為最基本的長飲型雞尾酒和短飲型雞尾酒兩種，前者長得像是餐廳喝果汁、牛奶的玻璃杯，這類調酒，像是 mojito，酒精濃度通常較低，適合慢慢喝，可以抓個 20 分鐘的時間喝完；後者長得像是倒三角的玻璃杯，多用來盛裝精濃度較高，像是馬丁尼等調酒，可以用 10 多分鐘的時間喝完它。<br><br>
