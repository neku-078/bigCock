				<p class="title" id="original">༺༒&nbsp;起&nbsp;&nbsp;源&nbsp;༒༻</p>
				<p class="content">最早出現「雞尾酒」字樣的印刷品是1803年4月28日出版的《The Farmer's Cabinet》。<br><br>
									之後在官方認可的印刷品中使用「雞尾酒」一詞的（也是最為人知的）是1806年5月13日紐約州哈德遜市出版的《Balance and Columbian Repository》，為雞尾酒是什麼提供了如下的答案：<br>
									「雞尾酒是組合了任何類型的烈酒、砂糖、水和苦酒的刺激烈酒，粗俗的人稱它為Bittered Sling，並認為它是極佳的競選良藥，因為它會使競選者更大膽和魯莽，同時使頭腦混亂。據說，對民主黨候選人亦有很大的幫助，因為咽下一杯雞尾酒的人會願意咽下任何其他東西（願意包容一切）。」<br><br>
									第一份包含有雞尾酒配方的酒保指導《How to Mix Drinks; or, The Bon Vivant's Companion》在1862年由Jerry Thomas負責出版，列了潘趣（Punches）、酸酒（Sours）、司令（Slings）、考比勒（Cobblers）、席拉布（Shrubs）、特迪（Toddies）、菲麗普（Flips）和許多其他種類的混合酒，總計十種和雞尾酒有關的配方，在手冊中指出了和其他飲品區隔的關鍵原料，即苦酒。<br><br>
									在美國禁酒時期（1919年－1933年），當擁有酒精變成了非法，雞尾酒在酒吧內仍然繼續被飲用，當時不只酒精品質遠低於過去所飲用的，連酒保應有的知識也大大地衰減。<br><br>
									雞尾酒的起源有多種傳說，較為可信的說法是1776年美國紐約一家酒館為了招攬顧客，用雞毛裝飾酒店，一日，當地一位紳士帶朋友來喝酒，微醉中叫服務生上一杯「雞尾」酒。服務生機智地倒了一杯由幾種不同的酒混合的飲料，為了不讓紳士醉倒，她加水沖淡了酒液，並隨手摘下一根雞毛貼在杯邊。紳士喝後覺得味道很好，便常來要雞尾酒喝，於是雞尾酒得以傳揚，另外一種說法是來自法語的Coquetier，這是在19世紀初紐奧良的一種雞蛋酒的名稱。</p>
